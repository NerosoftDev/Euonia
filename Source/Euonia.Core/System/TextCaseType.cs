namespace System;

/// <summary>
/// The text case type.
/// </summary>
public enum TextCaseType
{
	/// <summary>
	/// Represents no case.
	/// </summary>
	None,

	/// <summary>
	/// Represents upper case.
	/// </summary>
	Upper,

	/// <summary>
	/// Represents lower case.
	/// </summary>
	Lower,

	/// <summary>
	/// Represents title case.
	/// </summary>
	Title
}
